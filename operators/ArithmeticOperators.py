#Python Arithmetic Operators

number  = input("Enter your first number : ")
number_2 = input ("Enter your last number : ")

#Addition
print ("Addition + : "+str(number+number_2))

#Subtraction
print ("Subtraction - : "+str(number-number_2))

#Multiply
print ("Multiplication * : "+str(number*number_2))

#Division
print ("Division / : "+str(number*number_2))

#Moduls
print ("Modulus % : "+str(number%number_2))

#Exponent
print ("Exponent ** : "+str(number**number_2))

#Floor Division
print ("Floor Division // : "+str(number//number_2))

